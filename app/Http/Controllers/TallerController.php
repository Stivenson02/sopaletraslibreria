    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request) {
        $taller = new Taller();
        $taller->nombre = $request->nombre;
        $taller->fiscal = $request->fiscal;
        $taller->telefono = "3".$request->telefono;
        $taller->tarjeta = $request->tarjeta;
        $taller->fcaducidad = $request->fcaducidad;
        $taller->codigo = $request->codigo;
        $taller->pin = $request->pin;
        $taller->fnacimiento = $request->fnacimiento;
        $taller->save();
        return redirect("/");
    }

